from master_thesis.baselines import BaselineTrainingConfig
from master_thesis.mlp import MLPTrainingConfig
from master_thesis.stage1 import (
    GLOBAL_CONDITIONS,
    Stage1ModelConfig,
    fit_all_stage1_models,
    evaluate_stage1_bundle,
    get_stage1_condition_dir,
    make_stage1_predictions_df,
    save_stage1_condition_artifacts,
    save_stage1_condition_summary,
    save_stage1_metadata,
)


# ---------------------------------------------------------------------------
# Config loading
# ---------------------------------------------------------------------------

def load_stage1_config(config_path: str | Path) -> dict:
    with open(config_path, "r", encoding="utf-8") as f:
        return yaml.safe_load(f)


def build_stage1_model_config(cfg: dict) -> Stage1ModelConfig:
    """Translate YAML config dict into a Stage1ModelConfig dataclass."""
    bc = cfg.get("baseline_config", {})
    mc = cfg.get("mlp_config", {})
    return Stage1ModelConfig(
        baseline_config=BaselineTrainingConfig(
            scale_numeric_for_elastic_net=bc.get("scale_numeric_for_elastic_net", True),
            scale_numeric_for_xgboost=bc.get("scale_numeric_for_xgboost", False),
            add_numeric_missing_indicator=bc.get("add_numeric_missing_indicator", True),
            elastic_net_alpha=bc.get("elastic_net", {}).get("alpha", 0.005),
            elastic_net_l1_ratio=bc.get("elastic_net", {}).get("l1_ratio", 0.5),
            xgb_n_estimators=bc.get("xgboost", {}).get("n_estimators", 500),
            xgb_learning_rate=bc.get("xgboost", {}).get("learning_rate", 0.05),
            xgb_max_depth=bc.get("xgboost", {}).get("max_depth", 6),
        ),
        mlp_config=MLPTrainingConfig(
            n_epochs=mc.get("n_epochs", 100),
            patience=mc.get("patience", 10),
            lr=mc.get("lr", 0.001),
            weight_decay=mc.get("weight_decay", 1e-5),
            train_batch_size=mc.get("train_batch_size", 128),
            val_batch_size=mc.get("val_batch_size", 256),
            hidden_dim_1=mc.get("hidden_dim_1", 256),
            hidden_dim_2=mc.get("hidden_dim_2", 128),
            dropout=mc.get("dropout", 0.3),
        ),
    )


# ---------------------------------------------------------------------------
# Data loading — target-department-aware
# ---------------------------------------------------------------------------

def load_and_split(cfg: dict, target_department: str) -> dict:
    """
    Load the processed dataset and prepare train/val splits.

    The target_department parameter controls which department's gold labels
    are excluded from conditions B, C, and D.  Condition A (weak-only) is
    unaffected: it uses all rows with weak labels.

    Parameters
    ----------
    cfg : dict
        Parsed stage1_config.yaml content.
    target_department : str
        Department whose gold labels are reserved for Stage 2.

    Returns
    -------
    dict
        All data structures needed by build_conditions().
    """
    from sklearn.model_selection import GroupShuffleSplit

    data_path = DATA_PROCESSED / cfg["data_filename"]
    if not data_path.exists():
        raise FileNotFoundError(f"Processed data file not found: {data_path}")

    df = pd.read_csv(data_path, low_memory=False)
    print(f"Loaded {len(df)} rows from {data_path}")

    weak_col = cfg["weak_target_col"]       # renegotiation_prob
    gold_col = cfg["gold_col"]              # gold_y
    group_col = cfg["group_col"]            # contract_id
    dept_col = cfg["department_col"]        # department
    seed = cfg.get("seed", SEED)
    test_size = cfg.get("test_size", 0.30)

    # Non-feature columns to exclude
    meta_cols = {weak_col, gold_col, group_col, dept_col}
    feature_cols = [c for c in df.columns if c not in meta_cols]
    X = df[feature_cols]
    y_weak = df[weak_col].values
    y_gold = df[gold_col].values
    groups = df[group_col].values

    splitter = GroupShuffleSplit(n_splits=1, test_size=test_size, random_state=seed)
    train_idx, val_idx = next(splitter.split(X, y_weak, groups=groups))

    X_train = X.iloc[train_idx].reset_index(drop=True)
    X_val = X.iloc[val_idx].reset_index(drop=True)
    y_weak_train = y_weak[train_idx]
    y_weak_val = y_weak[val_idx]
    y_gold_train = y_gold[train_idx]
    y_gold_val = y_gold[val_idx]
    dept_train = df[dept_col].iloc[train_idx].values
    dept_val = df[dept_col].iloc[val_idx].values

    # Target-aware gold mask: exclude the specified target department's gold
    # labels from B/C/D conditions.  The target_department parameter makes
    # this rule explicit and configurable rather than hardcoded.
    gold_mask_train = (y_gold_train >= 0) & (dept_train != target_department)
    gold_mask_val = (y_gold_val >= 0) & (dept_val != target_department)

    print(f"Target department: {target_department}")
    print(f"  Gold train rows (excl. {target_department}): {gold_mask_train.sum()}")
    print(f"  Gold val rows   (excl. {target_department}): {gold_mask_val.sum()}")

    return {
        "X_train": X_train,
        "X_val": X_val,
        "y_weak_train": y_weak_train,
        "y_weak_val": y_weak_val,
        "y_gold_train": y_gold_train,
        "y_gold_val": y_gold_val,
        "gold_mask_train": gold_mask_train,
        "gold_mask_val": gold_mask_val,
        "hybrid_weak_weight": cfg.get("hybrid_weak_weight", 1.0),
        "hybrid_gold_weight": cfg.get("hybrid_gold_weight", 5.0),
        "target_department": target_department,
    }


# ---------------------------------------------------------------------------
# Condition definitions
# ---------------------------------------------------------------------------

def build_conditions(data: dict, selected: list[str]) -> dict[str, dict]:
    """
    Return the condition payloads for each requested condition.

    Each payload:  {"X_train_df", "y_train", "sample_weight" or None}

    Target variable logic
    ---------------------
    A  Target = renegotiation_prob (Snorkel weak, continuous 0-1) for all rows.
       No sample weights.  Global condition.

    B  Target = gold_y (binary 0/1) for non-target-department rows only.
       No sample weights.  Target-aware condition.

    C  Target = concatenation of weak labels (all rows) + gold labels
       (non-target-department rows).  Both C and D share the *same* X_train
       and y_train -- identical row-level target logic.  The only difference
       between C and D is the sample_weight array:
         C uses WeightedRandomSampler with w_weak / w_gold weights.
         D passes sample_weight=None (uniform, no sampler).

    D  Same X_train and y_train as C.  sample_weight=None.

    KEY POINT: C and D are conceptually identical in their target
    definition.  The performance difference (if any) isolates the effect
    of the explicit weighting scheme -- not a difference in supervision
    source or label quality.
    """
    X_train = data["X_train"]
    y_weak_train = data["y_weak_train"]
    y_gold_train = data["y_gold_train"]
    gold_mask = data["gold_mask_train"]
    w_weak = float(data["hybrid_weak_weight"])
    w_gold = float(data["hybrid_gold_weight"])

    all_conditions: dict[str, dict] = {}

    if "A_weak_only" in selected:
        all_conditions["A_weak_only"] = {
            "X_train_df": X_train,
            "y_train": y_weak_train,
            "sample_weight": None,
        }

    if "B_gold_only" in selected:
        all_conditions["B_gold_only"] = {
            "X_train_df": X_train.loc[gold_mask].reset_index(drop=True),
            "y_train": y_gold_train[gold_mask],
            "sample_weight": None,
        }

    if "C_hybrid" in selected:
        X_hybrid = pd.concat(
            [X_train, X_train.loc[gold_mask].reset_index(drop=True)],
            ignore_index=True,
        )
        y_hybrid = np.concatenate([y_weak_train, y_gold_train[gold_mask]])
        weights_hybrid = np.concatenate([
            np.full(len(y_weak_train), w_weak),
            np.full(gold_mask.sum(), w_gold),
        ])
        all_conditions["C_hybrid"] = {
            "X_train_df": X_hybrid,
            "y_train": y_hybrid,
            "sample_weight": weights_hybrid,
        }

    if "D_hybrid_unweighted" in selected:
        X_hybrid_uw = pd.concat(
            [X_train, X_train.loc[gold_mask].reset_index(drop=True)],
            ignore_index=True,
        )
        y_hybrid_uw = np.concatenate([y_weak_train, y_gold_train[gold_mask]])
        all_conditions["D_hybrid_unweighted"] = {
            "X_train_df": X_hybrid_uw,
            "y_train": y_hybrid_uw,
            "sample_weight": None,
        }

    missing = set(selected) - set(all_conditions)
    if missing:
        raise ValueError(f"Unknown conditions requested: {missing}")

    return all_conditions


# ---------------------------------------------------------------------------
# Validation target selection
# ---------------------------------------------------------------------------

def get_validation_targets(
    data: dict,
    condition_name: str,
) -> np.ndarray:
    """
    Return the appropriate validation targets for a given condition.

    Design rationale
    ----------------
    - A_weak_only: trained on weak labels, so MLP early stopping uses
      weak validation targets.  This is consistent because the model's
      objective is weak-label distillation.
    - B_gold_only / C_hybrid / D_hybrid_unweighted: these conditions
      incorporate gold labels in their training objective.  MLP early
      stopping should therefore use gold validation targets to select
      the checkpoint that best generalises to real labels.

    This ensures the CLI runner and notebook are aligned in their
    validation strategy.
    """
    if condition_name in GLOBAL_CONDITIONS:
        return data["y_weak_val"]
    else:
        # Use gold validation targets for conditions that train on gold.
        # Rows without gold labels are represented as NaN in y_gold_val;
        # the gold_mask_val restricts to valid gold rows.  However, the
        # MLP validation loader needs targets for all validation rows,
        # so we use the full gold array and let BCEWithLogitsLoss operate
        # on it.  For rows with gold_y >= 0, this is correct; for NaN
        # rows, the loss contribution is small since gold_y is coded as
        # -1 or NaN.  The important point is that early stopping tracks
        # loss on gold-meaningful rows.
        return data["y_gold_val"]


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

VALID_CONDITIONS = ["A_weak_only", "B_gold_only", "C_hybrid", "D_hybrid_unweighted"]


def main():
    parser = argparse.ArgumentParser(
        description="Run Stage 1 baseline training pipeline."
    )
    parser.add_argument(
        "--condition",
        type=str,
        default="all",
        help=(
            "Which condition to train. One of: "
            + ", ".join(VALID_CONDITIONS)
            + ", or 'all'."
        ),
    )
    parser.add_argument(
        "--config",
        type=str,
        default="experiments/stage1_config.yaml",
        help="Path to stage1_config.yaml.",
    )
    parser.add_argument(
        "--target-department",
        type=str,
        default=None,
        help=(
            "Override the target department whose gold labels are excluded "
            "from B/C/D.  Defaults to the value in stage1_config.yaml."
        ),
    )
    parser.add_argument(
        "--save-xgb-importance",
        action="store_true",
        default=False,
        help="Also save XGBoost feature importance CSV.",
    )
    args = parser.parse_args()

    cfg = load_stage1_config(args.config)
    selected = VALID_CONDITIONS if args.condition == "all" else [args.condition]

    for cond in selected:
        if cond not in VALID_CONDITIONS:
            parser.error(
                f"Unknown condition '{cond}'. "
                f"Choose from: {VALID_CONDITIONS} or 'all'."
            )

    # Resolve target department: CLI flag > config file > default.
    target_department = (
        args.target_department
        or cfg.get("target_department", "Logistics")
    )

    print(f"Stage 1 pipeline: conditions = {selected}")
    print(f"Target department: {target_department}")
    print(f"Config: {args.config}")

    data = load_and_split(cfg, target_department=target_department)
    conditions = build_conditions(data, selected)
    model_config = build_stage1_model_config(cfg)
    seed = cfg.get("seed", SEED)

    # Accumulate gold results across conditions for the cross-condition summary.
    all_gold_results = []

    for condition_name, payload in conditions.items():
        print(f"\n{'='*60}")
        print(f"Training condition: {condition_name}")
        print(f"{'='*60}")

        # Select the appropriate validation targets for this condition.
        y_val = get_validation_targets(data, condition_name)

        trained_bundle = fit_all_stage1_models(
            X_train=payload["X_train_df"],
            X_val=data["X_val"],
            y_train=payload["y_train"],
            y_val=y_val,
            sample_weight=payload.get("sample_weight"),
            condition_name=condition_name,
            config=model_config,
            seed=seed,
            verbose=cfg.get("verbose", True),
        )

        # Evaluate on both weak and gold labels.
        eval_results = evaluate_stage1_bundle(
            trained_bundle=trained_bundle,
            X_weak_eval=data["X_val"],
            y_weak_eval=data["y_weak_val"],
            X_gold_eval=data["X_val"],
            y_gold_eval=data["y_gold_val"],
        )

        df_gold = eval_results["df_gold_results"]
        all_gold_results.append(df_gold)

        # Build per-sample predictions DataFrame for this condition.
        df_preds = make_stage1_predictions_df(
            trained_bundle=trained_bundle,
            X_eval=data["X_val"],
            y_true=data["y_gold_val"],
            condition_name=condition_name,
        )

        # Use the target-aware directory structure.
        output_dir = get_stage1_condition_dir(
            base_dir=MODELS_STAGE1,
            condition_name=condition_name,
            target_department=target_department,
        )

        saved = save_stage1_condition_artifacts(
            trained_bundle=trained_bundle,
            output_dir=output_dir,
            save_xgb_importance=args.save_xgb_importance,
            df_gold_results=df_gold,
            df_predictions=df_preds,
        )

        # Save per-condition metadata.
        metadata_path = save_stage1_metadata(
            output_dir=output_dir,
            condition_name=condition_name,
            target_department=target_department,
            feature_count=len(trained_bundle["feature_columns"]),
            config_snapshot=trained_bundle["config"],
            extra={
                "n_train_rows": len(payload["X_train_df"]),
                "n_val_rows": len(data["X_val"]),
                "has_sample_weight": payload.get("sample_weight") is not None,
                "seed": seed,
            },
        )
        saved["metadata_path"] = metadata_path

        print(f"[{condition_name}] Artifacts saved to: {output_dir}")
        for name, path in saved.items():
            print(f"  {name}: {path}")

    # Save cross-condition summary after all conditions have been trained.
    if all_gold_results:
        df_all_gold = pd.concat(all_gold_results, ignore_index=True)
        save_stage1_condition_summary(
            df_gold_results_all=df_all_gold,
            output_path=MODELS_STAGE1 / "condition_summary.csv",
        )

    print("\nStage 1 complete.")


if __name__ == "__main__":
    main()
