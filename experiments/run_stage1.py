"""
run_stage1.py -- CLI runner for Stage 1 baseline training.

Trains all four Stage 1 models (Mean, ElasticNet, XGBoost, MLP) for
one or more supervision conditions and saves all artifacts.

Usage
-----
python experiments/run_stage1.py --condition A_weak_only
python experiments/run_stage1.py --condition B_gold_only
python experiments/run_stage1.py --condition C_hybrid
python experiments/run_stage1.py --condition D_hybrid_unweighted
python experiments/run_stage1.py --condition all

# Override the target department (default comes from stage1_config.yaml):
python experiments/run_stage1.py --condition all --target-department Logistics

The script:
1. Loads the processed dataset from data/processed/.
2. Prepares train/val splits and weak/gold targets.
3. Constructs the condition-specific training set and weights.
4. Calls fit_all_stage1_models() and evaluate_stage1_bundle().
5. Saves all artifacts to models/stage_1/{scope}/{condition}/.
6. Saves per-condition metadata.json for reproducibility.

Artifact path convention
------------------------
  models/stage_1/global/A_weak_only/           -- global condition
  models/stage_1/{target_dept}/B_gold_only/     -- target-aware conditions
  models/stage_1/{target_dept}/C_hybrid/
  models/stage_1/{target_dept}/D_hybrid_unweighted/

Requirements
------------
- data/processed/contract_with_features_labeled_with_gold.csv must exist.
- Weak labels (renegotiation_prob) and gold labels (gold_y) must be in that
  file (generated by notebooks 15 and 15.1 respectively).
"""

